List<ListObject> listData = [];
Map userData = {};
void getData(String userRole) async {
  String urlHead = 'http://192.168.1.108/dashboard/sf11/';
  final url = '${urlHead}queryDataForPermissions.php';
  var data = {
    'user_type': userRole,
  };
  userType = userRole;
  final response = await http.post(Uri.parse(url), body: data);
  
  var j = json.decode(response.body);
  List<ListObject> list=[];
  for (var k in j) {
    try {
      Widget? widget = getWidgets[k["permission_name"].toString().trim()]!;
        list.add(ListObject(k["permission_name"].toString(), widget)); 
    } catch (e) {
      EasyLoading.showToast(k["permission_name"],
          toastPosition: EasyLoadingToastPosition.bottom);
    }
  }
  listData = list;
}

List<ListObject> getList() => listData;
Map getUserData() => userData;

